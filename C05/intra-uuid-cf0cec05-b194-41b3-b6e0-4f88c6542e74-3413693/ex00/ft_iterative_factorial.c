int		ft_iterative_factorial(int n)
{
	int p;

	p = n - 1;
	if (n == 0)
		return (1);
	else if (n > 0)
	{
		while (p > 0)
		{
			n = n * p;
			p--;
		}
		return (n);
	}
	else
		return (0);
}
