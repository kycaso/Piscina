int		ft_is_prime(int nb)
{
	int i;
	int divisor;

	i = 2;
	if (nb == 1 || nb <= 0)
		return (0);
	while (i != nb)
	{
		divisor = nb / i;
		if ((divisor * i) == nb)
			return (0);
		i++;
	}
	return (1);
}
