void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int tab2[size];

	i = 0;
	while (i < size)
	{
		tab2[i] = *(tab + size - 1 - i);
		i++;
	}
	i = 0;
	while (i < size)
	{
		*(tab + i) = tab2[i];
		i++;
	}
}
