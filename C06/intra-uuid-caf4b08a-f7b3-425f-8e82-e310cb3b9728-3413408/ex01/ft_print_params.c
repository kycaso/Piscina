#include <unistd.h>

int		main(int n, char **argv)
{
	int i;
	int j;

	i = 1;
	while (i < n)
	{
		j = 0;
		while (argv[i][j] != '\0')
		{
			write(1, &argv[i][j], 1);
			j++;
		}
		write(1, "\n", 1);
		i++;
	}
}
