#include <unistd.h>

int		main(int n, char **argv)
{
	int i;

	(void)n;
	i = 0;
	while (argv[0][i] != '\0')
	{
		write(1, &argv[0][i], 1);
		i++;
	}
	write(1, "\n", 1);
}
