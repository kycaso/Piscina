#include <unistd.h>

int		main(int n, char **argv)
{
	int i;

	n--;
	while (n > 0)
	{
		i = 0;
		while (argv[n][i] != '\0')
		{
			write(1, &argv[n][i], 1);
			i++;
		}
		write(1, "\n", 1);
		n--;
	}
}
