int		ft_str_is_numeric(char *str)
{
	int		i;
	int		j;
	char	c;

	i = 0;
	j = 1;
	while (*(str + i) != '\0')
	{
		c = *(str + i);
		if (c < '0' || c > '9')
			j = 0;
		i++;
	}
	return (j);
}
