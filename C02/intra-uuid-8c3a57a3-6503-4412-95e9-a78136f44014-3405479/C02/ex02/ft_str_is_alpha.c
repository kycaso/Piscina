int		ft_str_is_alpha(char *str)
{
	int		i;
	int		j;
	char	c;

	i = 0;
	j = 1;
	while (*(str + i) != '\0')
	{
		c = *(str + i);
		if ((c < 'a' || c > 'z') && (c < 'A' || c > 'Z'))
			j = 0;
		i++;
	}
	return (j);
}
